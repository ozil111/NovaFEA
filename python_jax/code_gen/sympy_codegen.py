# 动态模型加载
# --------------------------------------------------------------------------- 
def _load_class(module_path: str, class_name: str):
    """动态加载类"""
    try:
        module = importlib.import_module(module_path)
        return getattr(module, class_name)
    except (ModuleNotFoundError, AttributeError):
        raise ImportError(f"Could not find class '{class_name}' in module '{module_path}'")

def load_element(name: str) -> Element:
    """Loads an element class from the definitions.elements directory."""
    class_name = name.capitalize()
    module_path = f"definitions.elements.{name}"
    return _load_class(module_path, class_name)()

def load_material(name: str) -> Material:
    """Loads a material class from the definitions.materials directory."""
    class_name = name.capitalize()
    module_path = f"definitions.materials.{name}"
    return _load_class(module_path, class_name)()


def _default_output(model_name: str, target: str) -> str:
    t = target.lower()
    if t == "jax":
        return f"{model_name}_gen.py"
    if t in ("cpp", "c++"):
        return f"{model_name}_gen.cpp"
    if t == "cuda":
        return f"{model_name}_gen.cu"
    return f"{model_name}_{t}.txt"

def main():
    parser = argparse.ArgumentParser(
        description="SymPy FEA 代码生成：输入 Element+Material，输出 cpp/cuda/jax 源码文件。"
    )
    parser.add_argument(
        "--element", "-e",
        required=True,
        help="单元名称 (e.g., 'tet4')",
    )
    parser.add_argument(
        "--material", "-m",
        required=True,
        help="材料名称 (e.g., 'isotropic')",
    )
    parser.add_argument(
        "--target", "-t",
        required=True,
        choices=["jax", "cpp", "cuda", "all"],
        help="目标：jax / cpp / cuda / all",
    )
    parser.add_argument(
        "--output", "-o",
        default=None,
        help="输出文件路径（默认根据 model+target 生成）",
    )
    args = parser.parse_args()

    # 1. Load models
    element = load_element(args.element)
    material = load_material(args.material)

    # 2. Get symbolic math model
    model = element.get_symbolic_model(material)
    
    # 3. Compile
    target = args.target
    if target == "all":
        if args.output is None:
            out_base = "."
        else:
            out_base = args.output
        out_dir = out_base
        generated = FEACompiler.compile_all(model)
        outputs = {
            "jax": f"{model.name}_gen.py",
            "cpp": f"{model.name}_gen.cpp",
            "cuda": f"{model.name}_gen.cu",
        }
        for t, code in generated.items():
            out_path = f"{out_dir.rstrip('/\\')}/{outputs[t]}"
            with open(out_path, "w", encoding="utf-8") as f:
                f.write(code)
            print(out_path)
    else:
        out_path = args.output or _default_output(model.name, target)
        code = FEACompiler.compile(model, target)
        with open(out_path, "w", encoding="utf-8") as f:
            f.write(code)
        print(out_path)


if __name__ == "__main__":
    main()